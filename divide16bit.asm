QUOTIENT_LOAD:
		LXI D, 0000H
	
DIVIDEND_CHECK:
		LHLD 1002H
		MOV B,H
		MOV C,L
		LDA 1009H
		CMP B
		JM STORE_Q_AND_R
		
		LDA 1008H
		CMP C
		JM STORE_Q_AND_R
		
DIVISION_BY_ADDITION:
		LHLD 1004H
		MOV B,H
		MOV C,L
		LHLD 1008H
		DAD B
		INX D
		SHLD 1008H
		JMP DIVIDEND_CHECK
		
STORE_Q_AND_R:
		SHLD 1008H		;STORE REMAINDER
		XCHG
		SHLD 1006H		;STORE QUOTIENT
		HLT